        15.4. Pravo na ispravak
        
        GDPR Vam daje pravo na ispravak bilo kojeg osobnog podatka koji je netočan ili nepotpun.
        
        15.5. Pravo na prenosivost podataka
        
        Imate pravo zahtijevati dostavu osobnih podataka koje ste nam stavili na raspolaganje u strojno čitljivom obliku.
        
        15.6. Pravo na prigovor
        
        Imate pravo uložiti prigovor na obradu svojih osobnih podataka kada ne postoji legitimni ili zakonski razlog da to činimo.
        
