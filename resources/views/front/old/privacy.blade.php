        Svoje pravo možete ostvariti u bilo kojem trenutku tako da nas kontaktirate (vidi ispod Kako nas možete kontaktirati).
        
        15.2. Pravo na pristup
        
        GDPR Vam daje pravo pitati nas koje Vaše osobne podatke imamo i kako ih obrađujemo te pravo dati Vam pristup tim podatcima.
        
        Svoje pravo možete ostvariti tako da nas kontaktirate (vidi ispod Kako nas možete kontaktirati). Uzmite u obzir da prije obrade bilo kojeg zahtjeva za pristup podatcima moramo potvrditi Vaš identitet, a moguće je i da Vas dodatno kontaktiramo kako bismo bili sigurni da smo razumjeli koje podatke zahtijevate. Nakon što potvrdimo Vaš identitet, tražene informacije ćemo Vam pružiti u roku od 30 dana.
        
